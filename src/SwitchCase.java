
public class SwitchCase {

	public static void main(String[] args) {
		int x = 3;
		switch(x) {
		case 1:
			System.out.println("value of x is 1");
		break;
		case 2:
			System.out.println("value of x is 2");
			break;
			
		default:
			System.out.println("value of x is other than 1 or 2");
			
		}

	}

}
